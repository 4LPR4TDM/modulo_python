#!/usr/bin/python3
#Entendiendo slices
valor_str = 'Pedro pecas pica papas'

print(valor_str[8]) #El valor dentro de los corchetes indica la rebanada que se va imprimir

print(valor_str[:]) #Dos puntos indica un indice inicial y un indice final

print(valor_str[8:10])

print(valor_str[0:10:2]) #del valor 0 al 10 se entrega de dos en dos

print(valor_str[::-1])